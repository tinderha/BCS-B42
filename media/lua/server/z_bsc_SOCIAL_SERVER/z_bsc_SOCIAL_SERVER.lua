local z_bsc_SOCIAL_SERVER = {};

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

function z_bsc_SOCIAL_SERVER.updateStatus(_plObj, _packet)
	local packet = _packet;
	sendServerCommand("z_bsc_SOCIAL", "updateStatus", packet);
end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

function z_bsc_SOCIAL_SERVER.sendMessage(_plObj, _packet)
	local packet = _packet;
	sendServerCommand("z_bsc_SOCIAL", "addLineInChat", packet);
end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

z_bsc_SOCIAL_SERVER.OnClientCommand = function(_module, _command, _plObj, _packet)
	if _module ~= "z_bsc_SOCIAL_SERVER" then return; end;
	z_bsc_SOCIAL_SERVER[_command](_plObj, _packet);
end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

Events.OnClientCommand.Add(z_bsc_SOCIAL_SERVER.OnClientCommand);

