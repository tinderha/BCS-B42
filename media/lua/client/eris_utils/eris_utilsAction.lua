local versionNumber = 0.1;

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------versionCheck

if eris_utilsAction then
	if eris_utilsAction.versionNumber >= versionNumber then
		return;
	end;
end;

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

require "TimedActions/ISBaseTimedAction"

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------

eris_utilsAction = ISBaseTimedAction:derive("eris_utilsAction");

eris_utilsAction.versionNumber = versionNumber;

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------isValid

function eris_utilsAction:isValid()
	return true;
end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------perform

function eris_utilsAction:perform()
	self.onCompleteFunc(self.targetObject);
	ISBaseTimedAction.perform(self)
end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------update

function eris_utilsAction:update()

end

----------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------new

function eris_utilsAction:new(_plObj, _targetObj, _onCompleteFunc, _stopWalk, _stopRun, _time)
	local o = {};

	setmetatable(o, self);

	self.__index = self;

	o.character = _plObj;

	o.targetObject = _targetObj;

	o.onCompleteFunc = _onCompleteFunc;

	o.stopOnWalk = _stopWalk;
	o.stopOnRun = _stopRun;

	o.maxTime = _time;

	return o;
end

